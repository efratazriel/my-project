CREATE TABLE PRODUCTS
(
PRODUCTID INT IDENTITY ,
HAIR_TYP VARCHAR (30)NULL,
COLOR VARCHAR (30)NULL,
LENGTH_WIG INT NOT NULL ,
PRICE MONEY NULL,
CONSTRAINT PROUDACTID_PK PRIMARY KEY (PRODUCTID)
)

GO
CREATE TABLE CAME_FROM
(
ID INT IDENTITY , 
NAMEOF VARCHAR (30) NULL,
FOLLOWERS INT NULL,
CITY VARCHAR(30) NULL,
CONSTRAINT ID_FK PRIMARY KEY (ID)
)

GO
CREATE TABLE CUSTOMERS
(
ENTRY_DATE DATE NOT NULL DEFAULT GETDATE(),
CUSTOMERID INT IDENTITY ,
NAME VARCHAR (30) NOT NULL DEFAULT 'Unknown',
PHONE INT NOT NULL DEFAULT 0000000000,
COUNTRY VARCHAR (30) NULL,
CITY VARCHAR (30) NULL,
CAME_FROM INT NULL,
CONSTRAINT COSTUMERID_PK PRIMARY KEY (CUSTOMERID),
CONSTRAINT CAME_FROM_FK FOREIGN KEY (CAME_FROM) REFERENCES CAME_FROM(ID)
)

GO
CREATE TABLE ORDERS
(
ORDERID INT IDENTITY,
CUSTOMERID INT NULL,
ORDERDATE DATE NOT NULL DEFAULT GETDATE(),
PRODUCTID INT NULL,
ADVANCE_PYMENT MONEY NOT NULL DEFAULT 3000,
SHIPPING_DATE DATE NULL,
SHIPPING_METHODS VARCHAR(30) NULL,
CONSTRAINT ORDERID_PK PRIMARY KEY (ORDERID),
CONSTRAINT CUSTOMERID_FK FOREIGN KEY (CUSTOMERID) REFERENCES CUSTOMERS(CUSTOMERID),
CONSTRAINT PRODUCTID_FK FOREIGN KEY (PRODUCTID) REFERENCES PRODUCTS(PRODUCTID),
)

GO
CREATE TABLE EMPLOYEES
(
EMPLOYEEID INT IDENTITY ,
FIRSTNAME VARCHAR (30) NULL,
LASTNAME VARCHAR (30) NULL,
PHONENUMBER INT NULL ,
CITY VARCHAR (30) NULL,
SPECIALIZED VARCHAR (30) NULL ,
WORKING_ON_ORDERID INT NULL,
CONSTRAINT EMLOYEEDID_PK PRIMARY KEY (EMPLOYEEID),
CONSTRAINT WORKING_ON_ORDERID_FK FOREIGN KEY (WORKING_ON_ORDERID) REFERENCES ORDERS(ORDERID)
)

GO
CREATE TABLE ORDER_TRACKING 
(
ID INT IDENTITY,
ORDERID INT NULL ,
EXIT_TO_COLOR DATE NULL,
BACK_FROM_COLOR DATE NULL,
EXIT_TO_HANDMADE DATE NULL,
BACK_FROM_HANDMADE DATE NULL,
COMPLETED VARCHAR (3) NULL,
CONSTRAINT ID_PK PRIMARY KEY (ID),
CONSTRAINT ORDERID_FK FOREIGN KEY (ORDERID) REFERENCES ORDERS(ORDERID)
)
GO
